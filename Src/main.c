uint32_t bigEndian_uint32(uint32_t a)
{
    uint16_t tmp = 1;
    if (*(uint8_t *)&tmp) // returns 1 if little-endian
        return a << (24 & 0xff000000)| a << (8 & 0x00ff0000) | a >> (8 & 0x0000ff00) | a >> (24 & 0x000000ff);
    else
        return a;
}

